// isPalindrome проверяет, является ли число палиндромом без выделения памяти в куче
// Сложность по времени: O(log10(N))
// Сложность по памяти: O(1) — 0 аллокаций
func isPalindrome(x int) bool {
	// Побитовый сдвиг проверяет знаковый бит (отрицательные числа не палиндромы)
	// Также исключаем числа, оканчивающиеся на 0 (кроме самого нуля)
	if (x >> 31) != 0 || (x%10 == 0 && x != 0) {
		return false
	}

	num := uint32(x)
	var revNum uint32 = 0

	// Инвертируем только половину числа во избежание переполнения uint32
	for num > revNum {
		revNum = revNum*10 + num%10
		num /= 10
	}

	return num == revNum || num == revNum/10
}
